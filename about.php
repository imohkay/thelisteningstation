    <script>
      document.addEventListener('DOMContentLoaded', function () {
        const teamCards = document.querySelectorAll('.js-about-team-card');
        const modal = document.querySelector('.js-about-team-detail-modal');
        const closeButton = document.querySelector('.js-about-team-detail-close');
        const teamGrid = document.querySelector('.about-team-grid');

        if (!teamCards.length || !modal) return;

        const detailPhoto = modal.querySelector('.js-about-team-detail-photo');
        //const detailPhotoWrapper = modal.querySelector('.about-team-detail-photo-wrapper');
        const detailName = modal.querySelector('.js-about-team-detail-name');
        const detailTitle = modal.querySelector('.js-about-team-detail-title');
        const detailDescription = modal.querySelector('.js-about-team-detail-description');

        function openModal(card) {
          const name = card.getAttribute('data-name');
          const title = card.getAttribute('data-title');
          const description = card.getAttribute('data-description');
          const photo = card.getAttribute('data-photo');
          //const bgImage = card.getAttribute('data-bg-image');

          if (detailPhoto) detailPhoto.src = photo || '';
          if (detailPhoto) detailPhoto.alt = name || 'Team member';
          // if (detailPhotoWrapper && bgImage) {
          //   detailPhotoWrapper.style.backgroundImage = `url('${bgImage}')`;
          //   detailPhotoWrapper.style.backgroundSize = 'cover';
          //   detailPhotoWrapper.style.backgroundPosition = 'center';
          // }
          if (detailName) detailName.textContent = name || '';
          if (detailTitle) detailTitle.textContent = title || '';
          if (detailDescription) detailDescription.textContent = description || '';

          card.classList.add('about-team-card--active');
          if (teamGrid) teamGrid.classList.add('about-team-grid--blurred');

          modal.setAttribute('aria-hidden', 'false');
          if (closeButton) closeButton.focus();
        }

        function closeModal() {
          modal.setAttribute('aria-hidden', 'true');

          teamCards.forEach(card => {
            card.classList.remove('about-team-card--active');
          });

          if (teamGrid) teamGrid.classList.remove('about-team-grid--blurred');
        }

        teamCards.forEach(card => {
          card.addEventListener('click', function () {
            openModal(card);
          });
        });

        if (closeButton) {
          closeButton.addEventListener('click', function (e) {
            e.stopPropagation();
            closeModal();
          });
        }

        document.addEventListener('keydown', function (e) {
          if (e.key === 'Escape' && modal.getAttribute('aria-hidden') === 'false') {
            closeModal();
          }
        });
      });
    </script>
