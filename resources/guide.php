          <div class="container-xs margin-reset padding-reset">
            <ol>
              <li class="spacing-4-b-app">
                <div class="font-semi-bold">Take a Break</div>
                Give yourself permission to rest. Step away from study materials and screens for a while. Even short walks or a nap can help reset your mind.
              </li>
              <li class="spacing-4-b-app">
                <div class="font-semi-bold">Reconnect with Hobbies</div>
                Return to activities that bring you joy—drawing, music, reading, or sports. Engaging in hobbies can help you decompress and restore energy.
              </li>
              <li class="spacing-4-b-app">
                <div class="font-semi-bold">Practice Mindfulness</div>
                Try meditation, deep breathing exercises, or mindful walking. Being present in the moment can reduce lingering anxiety and calm racing thoughts.
              </li>
              <li class="spacing-4-b-app">
                <div class="font-semi-bold">Talk About Your Feelings</div>
                Share your experiences with friends, family, or a counselor. Expressing how you feel can lighten emotional load and provide perspective.
              </li>
              <li class="spacing-4-b-app">
                <div class="font-semi-bold">Maintain a Routine</div>
                Even after exams, try to stick to regular sleeping, eating, and activity schedules. A structured routine helps your body and mind recover.
              </li>
              <li class="spacing-4-b-app">
                <div class="font-semi-bold">Move Your Body</div>
                Exercise is a proven stress reliever. Whether it’s a jog, yoga, or stretching, physical activity releases endorphins and boosts mood.
              </li>
              <li class="spacing-4-b-app">
                <div class="font-semi-bold">Celebrate Achievements</div>
                Acknowledge your effort, not just results. Reward yourself for the hard work, small wins, and milestones reached.
              </li>
              <li class="spacing-4-b-app">
                <div class="font-semi-bold">Limit Social Media</div>
                Post-exam stress can be amplified by social comparison. Take breaks from scrolling and focus on activities that make you feel good.
              </li>
              <li class="spacing-4-b-app">
                <div class="font-semi-bold">Reflect, Don’t Ruminate</div>
                Think about what you learned from the exam experience, but avoid overthinking mistakes. Reflection helps growth, rumination fuels anxiety.
              </li>
              <li class="spacing-4-b-app">
                <div class="font-semi-bold">Seek Support if Needed</div>
                If stress or anxiety persists, reach out to a counselor or a mental health professional. Asking for help is a sign of strength.
              </li>
            </ol>

            <h3 class="h2">Conclusion:</h3>
            <p>
              Exams may end, but taking care of your emotional and mental health should continue. By practicing these strategies, you can recover, recharge, and prepare for whatever comes next with confidence and resilience.
